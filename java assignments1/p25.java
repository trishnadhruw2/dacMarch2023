public class p25 {
    public static void main(String []args){
        Float num1 = 3.1415f;
    
        // Convert Float instance to byte
        byte b = num1.byteValue();
        System.out.println(b);
        
        // Convert Float instance to short
        short s = num1.shortValue();
        System.out.println(s);
        
        // Convert Float instance to int
        int i = num1.intValue();
        System.out.println(i);
        
        // Convert Float instance to long
        long l = num1.longValue();
        System.out.println(l);
        
        // Convert Float instance to float
        float f = num1.floatValue();
        System.out.println(f);
        
        // Convert Float instance to double
        double d = num1.doubleValue();
        System.out.println(d);
    }
    
}
